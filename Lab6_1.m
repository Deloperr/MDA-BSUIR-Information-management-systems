% Тема: Многомерно-матричные экспоненты
clc; 
clear;  

% Объявляем символьную переменную
syms t;

% 1. Определяем функцию
%    Для данного варианта экспонента сводится к exp(t)
f = exp(t);

% 2. Разложение функции exp(t) в ряд Тейлора
%    до 5-го порядка в окрестности точки t=0
taylor_f = taylor(f, t, 'ExpansionPoint', 0, 'Order', 6);
disp('Разложение экспоненты в ряд Тейлора (до 5-го порядка):');
disp(taylor_f);

% 3. Сравнение exp(t) и её разложения
T = -2:0.1:2;                          % область определения
F = double(subs(f, t, T));             % значения exp(t)
F_taylor = double(subs(taylor_f, t, T)); % значения ряда Тейлора

% 4. Построение графиков в 2D
figure;
plot(T, F, 'r-', 'LineWidth', 2);       % исходная экспонента
hold on;
plot(T, F_taylor, 'b--', 'LineWidth', 2); % аппроксимация рядом Тейлора
legend('exp(t)','Ряд Тейлора');
title('Сравнение exp(t) и её аппроксимации рядом Тейлора');
grid on;

% 5. Дополнительно: 3D график
%    Используем exp(X+Y) как двумерное расширение
[X,Y] = meshgrid(-2:0.1:2, -2:0.1:2);
Z = exp(X+Y); % пример двумерной "матричной экспоненты"
figure;
mesh(X,Y,Z);
title('3D поверхность exp(X+Y)');
grid on;
