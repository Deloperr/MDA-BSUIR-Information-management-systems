clc; clear; close all;

% Задаем параметры
n = 4;          % Порядок матриц
p = 4;          % Размерность матрицы A
q = 3;          % Размерность матрицы B
lambda = 1;     % Параметр λ
mu = 3;         % Параметр μ
T_perm = [3, 2, 4, 1]; % Перестановка для транспонирования

fprintf('n = %d, p = %d, q = %d\n', n, p, q);
fprintf('λ = %d, μ = %d\n', lambda, mu);
fprintf('T = [%s]\n\n', num2str(T_perm));

% 1. Сформируем матрицы A и B
A = rand(n, n) * 4;     % p-мерная матрица A (размер 4x4)
B = rand(n, q) * 4;     % q-мерная матрица B (размер 4x3)

% 2. Получаем транспонированную матрицу A^T относительно перестановки T
% T = [3, 2, 4, 1] означает: берем строки в порядке 3,2,4,1
A_transposed = A(T_perm, :);

% 3. Получаем матрицу D равную (λ,μ)-свернутому произведению матриц A и B
% Для матриц: (1,1)-свернутое произведение - это обычное матричное умножение
D = A * B;

% 4. Сформируем (λ,μ)-единичную матрицу E(λ,μ) n-го порядка
% Для матриц: (1,1)-единичная матрица - это обычная единичная матрица
E = eye(n);

% Находим произведение F = {λ,μ}(E(λ,μ)B)
% Это эквивалентно E * B (матричное умножение)
F = E * B;

% Выводим результаты
disp('1. Матрица A (4x4):');
disp(A);
fprintf('\n');

disp('2. Матрица B (4x3):');
disp(B);
fprintf('\n');

disp('3. Транспонированная матрица A^T (по перестановке T=[3,2,4,1]):');
disp(A_transposed);
fprintf('   Размер: %dx%d\n', size(A_transposed, 1), size(A_transposed, 2));
fprintf('\n');

disp('4. Свернутое произведение D = A * B (4x3):');
disp(D);
fprintf('   Размер: %dx%d\n', size(D, 1), size(D, 2));
fprintf('\n');

disp('5. (λ,μ)-единичная матрица E(λ,μ) (4x4):');
disp(E);
fprintf('\n');

disp('6. Произведение F = E * B (4x3):');
disp(F);
fprintf('   Размер: %dx%d\n', size(F, 1), size(F, 2));
fprintf('\n');